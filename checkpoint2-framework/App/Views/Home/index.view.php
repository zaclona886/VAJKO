<div class="row">
    <div class="col-sm-6 col-xs-12 col-md-12">
        <p class="p-2">A powerful fox known as the Nine-Tails attacks Konoha, the hidden leaf village in the Land of Fire, one
            of the Five Great Shinobi Countries in the Ninja World. In response, the leader of Konoha and the Fourth
            Hokage, Minato Namikaze (with his wife Kushina Uzumaki) seals the fox inside the body of his newborn
            son, Naruto Uzumaki, making Naruto a host of the beast; this costs Naruto's father his life, and the
            Third Hokage returns from retirement to become the leader of Konoha again. Naruto is often scorned by
            Konoha's villagers for being the host of the Nine-Tails. Due to a decree by the Third Hokage forbidding
            any mention of these events, Naruto learns nothing about the Nine-Tails until 12 years later, when
            Mizuki, a renegade ninja, reveals the truth to Naruto. Naruto then defeats Mizuki in combat,earning the
            respect of his teacher, Iruka Umino.
        </p>
